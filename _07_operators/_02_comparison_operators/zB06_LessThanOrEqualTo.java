public class zB06_LessThanOrEqualTo {
    
    public static void main(String[] args) {
        
        // ob a kleiner als b
        int a = 4;
        int b = 16;
        
        if ( a <= b ) {
            System.out.print("a is Less Than Or Equal To b");
        }
        System.out.print("\n");
        
        
        // ob a gleich b
        a = 4;
        b = 4;
        
        if ( a <= b ) {
            System.out.print("a is Less Than Or Equal To b");
        }
        System.out.print("\n");
        
    }
    
}
